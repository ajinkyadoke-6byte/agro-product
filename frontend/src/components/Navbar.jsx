import React from 'react'
import logo from '../assets/AgroMartlogo.jpeg'
import searchIcon from '../assets/search_icon.jpeg'
import bellIcon from '../assets/bell_icon.jpeg'
import cartIcon from '../assets/cart_icon.jpeg'
import profileIcon from '../assets/profile_icon.jpeg'
import downButton from '../assets/down_button.jpeg'

const Navbar = ({ active, setActive, cartCount = 0 }) => {
  const links = ['Home', 'Product', 'Categories', 'About Us', 'Contact Us']

  return (
    <div className="navbar">
      <div className="navbar-top">
        <div className="logo">
          <img src={logo} alt="AgroMart" />
          <span>AgroMart</span>
        </div>

        <div className="search-bar">
          <input type="text" placeholder="Search for products..." />
          <img src={searchIcon} alt="search" className="search-icon" />
        </div>

        <div className="header-icons">
          <div className="icon-wrap">
            <img src={bellIcon} alt="notifications" className="nav-icon" />
            <span className="notif-dot"></span>
          </div>
          <img
            src={profileIcon}
            alt="profile"
            className="nav-icon"
            style={{ cursor: 'pointer' }}
            onClick={() => setActive('Login')}
          />
          <div
            className="icon-wrap"
            onClick={() => setActive('Cart')}
            style={{ cursor: 'pointer' }}
          >
            <img src={cartIcon} alt="cart" className="nav-icon" />
            {cartCount > 0 && <span className="cart-badge">{cartCount}</span>}
          </div>
        </div>
      </div>

      <div className="nav-links">
        {links.map((link) => (
          <span
            key={link}
            className={active === link ? 'nav-active' : ''}
            onClick={() => setActive(link)}
          >
            {link}
            {link === 'Categories' && (
              <img src={downButton} alt="dropdown" className="dropdown-arrow" />
            )}
          </span>
        ))}
      </div>
    </div>
  )
}

export default Navbar